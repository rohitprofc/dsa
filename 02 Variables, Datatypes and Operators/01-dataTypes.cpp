#include <iostream>
using namespace std;

int main() {
    int integerVar = 10;          // Integer data type
    float floatVar = 5.5f;        // Float data type
    char charVar = 'A';           // Character data type
    bool boolVar = true;          // Boolean data type
    double doubleVar = 20.123456; // Double data type

    cout << "Integer: " << integerVar << endl;
    cout << "Float: " << floatVar << endl;
    cout << "Character: " << charVar << endl;
    cout << "Boolean: " << boolVar << endl;
    cout << "Double: " << doubleVar << endl;

    return 0;
}
